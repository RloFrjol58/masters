    void consultar(){
        String sql = "select * from servicios";
        
        try{
            conet = con1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] servicios = new Object[3];
            modeloSE = (DefaultTableModel) tabla1.getModel();
            
            while (rs.next()){
                servicios [0] = rs.getInt("id");
                servicios [1] = rs.getString("servicio");
                servicios [2] = rs.getString("precio");
                
                modeloSE.addRow(servicios);
            }
            tabla1.setModel(modeloSE);
            
        }catch(Exception e){
            
        }
    }
    
    

    
