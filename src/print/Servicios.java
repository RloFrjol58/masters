    void borrar(){
        
        Boolean borrar = true;
        String mensaje = "";
        String ID = txtID.getText();
         
        if(ID.length()==0){
            borrar = false;
            mensaje += "Escriba el ID\n";
        }
         
        if (borrar) {
           BorrarUS control = new BorrarUS();
           control.borrar(ID);
        }else JOptionPane.showMessageDialog(null, mensaje);
    }
    
    public void LimpiarTabla(){
        while (modeloSE.getRowCount() > 0) {
            modeloSE.removeRow(0);
    }
    }
    
    void agre (){
        
        Boolean guardar = true;
        String mensaje = "";
        String servicio = txtServicio.getText();
        String precio = txtPrecio.getText();
       
        //conditionals of data insert
        if(servicio.length()==0){
            guardar = false;
            mensaje += "Escriba el apellido\n";
        }
        if(precio.length()==0){
            guardar = false;
            mensaje += "Escriba el nombre\n";
        }
        
        if(guardar){
          ControlDatosSE control = new ControlDatosSE();
          control.insertar(servicio, precio);
          txtID.setText("1");
          txtServicio.setText("");
          txtPrecio.setText("");
          
        }else JOptionPane.showMessageDialog(null, mensaje);
        
    }


